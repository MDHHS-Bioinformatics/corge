#!/usr/bin/env python


import argparse
import pandas as pd
import sys

def format_linkages(df, threshold, max_distance, include_threshold=False):
    """
    Process a DataFrame to identify and format linkages (connections) based on distance thresholds.

    Parameters:
    - df (pd.DataFrame): DataFrame with distances between samples. Rows and columns represent samples.
    - threshold (float): Minimum distance for a sample to be considered a linkage.
    - max_distance (float): Maximum distance for a sample to be considered a linkage.
    - include_threshold (bool): If True, include distances equal to the threshold; defaults to False.

    Returns:
    - pd.Series: A Series where each row contains a formatted string of linkages or 'no linkages' if none are found.
    """
    # Define a nested function to process each row in the DataFrame
    def linkages(distances):
        """
        Identify valid linkages for a single row (sample) based on distance thresholds.
        Parameters:
        - distances (pd.Series): A row of distances from the DataFrame.
        Returns:
        - str: A formatted string listing valid linkages and their distances, or 'no linkages'.
        """
        # Filter distances to find valid linkages
        filtered = [
            f"{sample} ({distances[sample]})"  # Format as 'sample_name (distance)'
            for sample in distances.index  # Iterate over all sample names
            if (
                (distances[sample] > threshold if not include_threshold else distances[sample] >= threshold)  # Check min threshold
                and distances[sample] <= max_distance  # Check max threshold
                and sample != distances.name  # Exclude self-linkages
            )
        ]

        # Return formatted linkages as a comma-separated string or 'no linkages'
        return ', '.join(filtered) if filtered else 'no linkages'

    # Apply the linkages function to each row in the DataFrame
    return df.apply(linkages, axis=1)

def main(argv=None):
    #use sys.argv if argv is not passed
    if argv is None:
        argv = sys.argv[1:]
    #intializle parser argumetns
    parser = argparse.ArgumentParser(
        description="Script to determine linkages based on the outputs of Reportree"
    )
    parser.add_argument(
        "distance_matrix", type=str, help="Path to the _dist_hamming.tsv generated by reportree"
    )
    parser.add_argument(
        'species', type=str,help="Name of the species from which the input file was generated from"
    )
    args = parser.parse_args(argv)

    #Read the dataframe
    df = pd.read_csv(args.distance_matrix, sep='\t',index_col = 0)
    # Exclude self-comparisons by setting diagonal elements to a large value that won't meet the threshold
    for i in range(len(df)):
        df.iloc[i, i] = float('inf')
    #Get strong and intermediate linkages
    strong_linkages = format_linkages(df,0,10, include_threshold=True)
    intermediate_linkages = format_linkages(df, 10, 40)
    #get the species value
    species = args.species.replace(' ','_')
    #create a results dataframe for the species
    species_results = pd.DataFrame({
        'sampled_id' : df.index,
        'species': species,
        'strong_linkages' : strong_linkages,
        'intermediate_linkages' : intermediate_linkages

    })
    #save the results to a file
    species_results.to_csv(f"{species}_linkages_table.csv",index=False)

if __name__ == "__main__":
    main()
